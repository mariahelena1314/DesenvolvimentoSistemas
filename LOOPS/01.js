// /*Loops ou laços de repetição*/

// enquanto isso, faça aquilo
let cont = 0;

while(cont < 10){
    console.log(cont);
    cont = cont + 1; // incrementa de 1 em 1
}
console.log('Fim do laço')

// Doo... while(Faça enquanto)
do {
   console.log('Estou executando: ' + cont);
   cont ++;
} while (cont < 5);

// Lacços de repetição com for

for(let i = 0; i < 10; i++){
    console.log(i);
}