// 21. Faça um programa que leia um número e exiba o dia correspondente da semana. (1-Domingo, 2- Segunda, etc.), se digitar outro valor deve aparecer valor inválido.

let numero = 9;

if ( numero == 1){
    console.log(' Segunda-Feira!'); 
}
else if ( numero == 2){
    console.log(' Terça-Feira!'); 
}
else if ( numero == 3){
    console.log(' Quarta-Feira!'); 
}
else if ( numero == 4){
    console.log(' Quinta-Feira!'); 
}
else if ( numero == 5){
    console.log(' Sexta-Feira!'); 
}
else if ( numero == 6){
    console.log(' Sabado!'); 
}
else if ( numero == 7){
    console.log(' Domingo!'); 
}
else {
    console.log(' Valor invalido!');
    
}